package com.emp.DTO;

public class Department {
private int dno;
private String dname;
private int lid;
private int count;
public int getDno() {
	return dno;
}
public void setDno(int dno) {
	this.dno = dno;
}
public String getDname() {
	return dname;
}
public void setDname(String dname) {
	this.dname = dname;
}
public int getLid() {
	return lid;
}
public void setLid(int lid) {
	this.lid = lid;
}
public int getCount() {
	return count;
}
public void setCount(int count) {
	this.count = count;
}

}
